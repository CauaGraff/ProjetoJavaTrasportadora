package ProjetoTrasportadora;

import java.util.Date;

public class Reboque {
	
	// ATRIBUTOS
	
	private String placa;
	
	private Number renavam;
	
	private Date ano;
	
	private Number capacidade;
	
	// METODOS GET E SET

	public String getPlaca() {
		return placa;
	}

	public void setPlaca(String placa) {
		this.placa = placa;
	}

	public Number getRenavam() {
		return renavam;
	}

	public void setRenavam(Number renavam) {
		this.renavam = renavam;
	}

	public Date getAno() {
		return ano;
	}

	public void setAno(Date ano) {
		this.ano = ano;
	}

	public Number getCapacidade() {
		return capacidade;
	}

	public void setCapacidade(Number capacidade) {
		this.capacidade = capacidade;
	}
	
	
	
	
}
